/* 
 * addOK - Determine if can compute x+y without overflow
 *   Example: addOK(0x80000000,0x80000000) = 0,
 *            addOK(0x80000000,0x70000000) = 1, 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int addOK(int x, int y) {
  int xy =x+y; // sum x,y
  int xshift=x>>31; //right shift x
  int yshift=y>>31; //right shift y 
  int xyshift=xy>>31; //right shift sum
  return (!! (xshift^yshift) )| (!(xshift^xyshift) &(!(yshift^xyshift)));
}
